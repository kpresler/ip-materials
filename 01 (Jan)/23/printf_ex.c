#include <stdio.h>

int main(void){
    int first = 10;
    float second = 20;

    printf("first, printed as an int: %d\n", first);
    printf("first, printed as a float: %f\n", first);

    printf("second, printed as a float: %f\n", second);
    printf("second, printed as an int: %d\n", second);


    return 0;

}
